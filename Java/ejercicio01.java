public class ejercicio01 {
    public static void main(String[] args) {
        
        // NUMEROS
        byte var1 = 1;
        short var2 = 10;
        int var3 = 234;
        long var4 = 123;
        // DECIMALES
        float var5 = 23.4f;
        double var6 = 23.2;
        // BOLEANOS
        boolean var7 = false;
        boolean var8 = true;
        // TEXTO
        String var10 = "Hello World";
        
        System.out.println("NUMEROS ------------------");
        System.out.println("byte: " + var1 + "\nshort: " + var2 + "\nint: " + var3 + "\nlong: " + var4 + "\n");
        System.out.println("DECIMALES ----------------");
        System.out.println("float: " + var5 + "\ndouble: " + var6 + "\n");
        System.out.println("BOOLEANOS ----------------");
        System.out.println("boolean: " + var7 + "\nboolean: " + var8 + "\n");
        System.out.println("TEXTO --------------------");
        System.out.println("String: " + var10);
    }    
}
